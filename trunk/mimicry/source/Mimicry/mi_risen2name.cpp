MIBool mCRisen2Name::s_bInitialized = MIFalse;
mTNameMap< MIU32 > mCRisen2Name::s_mapRisen2Hashes;
mTMap< MIU32, mCName > mCRisen2Name::s_mapRisen2Names;

mCRisen2Name::mCRisen2Name( mCString const & a_strString ) :
    mCName( a_strString )
{
    if ( !s_bInitialized )
        Initialize();
}

mCRisen2Name::mCRisen2Name( mCName const & a_nameSource ) :
    mCName( a_nameSource )
{
    if ( !s_bInitialized )
        Initialize();
}

mCIOStreamBinary & mCRisen2Name::operator << ( mCIOStreamBinary & a_streamSource )
{
    MIU32 u32ID = a_streamSource.ReadU32();
    mCName const & nameSource = s_mapRisen2Names[ u32ID ];
    if ( !nameSource.IsValid() )
        MI_ERROR( mCRisen2StreamError, EUnknownHash, "Unknown Risen 2 hash." );
    *this = nameSource;
    return a_streamSource;
}

mCIOStreamBinary & mCRisen2Name::operator >> ( mCIOStreamBinary & a_streamDest ) const
{
    a_streamDest << GetRisen2ID();
    return a_streamDest;
}

MIU32 mCRisen2Name::GetRisen2ID( void ) const
{
    return s_mapRisen2Hashes[ *this ];
}

void mCRisen2Name::RegisterRisen2NameStrings( MILPCChar const * a_pStrings, MIUInt a_uCount )
{
    s_mapRisen2Hashes.Reserve( a_uCount * 4 / 3 + s_mapRisen2Hashes.GetCount() );
    s_mapRisen2Names.Reserve( a_uCount * 4 / 3 + s_mapRisen2Names.GetCount() );
    for ( MIUInt u = a_uCount; u--; )
    {
        mCName nameTemp( a_pStrings[ u ] );
        MIU32 u32Hash = g_djb2( a_pStrings[ u ] );
        if ( !u32Hash )
            MI_ERROR( mCError, EGeneral, "Zero hash." );
        mCName & nameDest = s_mapRisen2Names[ u32Hash ];
        if ( nameDest.GetID() )
            MI_ERROR( mCError, EGeneral, "Hash collision." );
        nameDest = nameTemp;
        s_mapRisen2Hashes[ nameTemp ] = u32Hash;
    }
}

mCIOStreamBinary & operator >> ( mCIOStreamBinary & a_streamSource, mCRisen2Name & a_nameDest )
{
    return ( a_nameDest << a_streamSource );
}

mCIOStreamBinary & operator << ( mCIOStreamBinary & a_streamDest, mCRisen2Name const & a_nameSource )
{
    return ( a_nameSource >> a_streamDest );
}

